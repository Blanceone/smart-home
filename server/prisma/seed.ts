async function main() {
  console.log('开始导入设备数据...');
  
  for (const device of devices) {
    const created = await prisma.device.create({
      data: {
        name: device.name,
        brand: device.brand,
        category: device.category,
        price: device.price,
        originalPrice: device.originalPrice,
        description: device.description,
        features: device.features,
        specifications: device.specifications,
        applicableScenes: device.applicableScenes,
        imageUrl: device.imageUrl,
        taobaoItemId: device.taobaoItemId,
        taobaoUrl: device.taobaoUrl,
        status: 1,
      },
    });
    console.log(`已创建: ${created.name}`);
  }
  
  console.log(`\n完成！共导入 ${devices.length} 个设备`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
